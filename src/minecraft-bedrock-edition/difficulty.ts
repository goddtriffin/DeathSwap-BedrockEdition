/**
 * `Difficulty` defines the possible game difficulties.
 */
export enum Difficulty {
  /**
   * `Peaceful` is peaceful difficulty.
   */
  Peaceful = "peaceful",

  /**
   * `Easy` is easy difficulty.
   */
  Easy = "easy",

  /**
   * `Normal` is normal difficulty.
   */
  Normal = "normal",

  /**
   * `Hard` is hard difficulty.
   */
  Hard = "hard",
}
