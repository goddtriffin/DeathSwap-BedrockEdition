/**
 * `Time` defines the possible game times.
 */
export enum Time {
  /**
   * `Day` is day.
   */
  Day = "day",

  /**
   * `Night` is night.
   */
  Night = "night",

  /**
   * `Noon` is noon.
   */
  Noon = "noon",

  /**
   * `Midnight` is midnight.
   */
  Midnight = "midnight",

  /**
   * `Sunrise` is sunrise.
   */
  Sunrise = "sunrise",

  /**
   * `Sunset` is sunset.
   */
  Sunset = "sunset",
}
