/**
 * `Gamemode` defines the possible world/player gamemodes.
 */
export enum Gamemode {
  /**
   * `Survival` is survival mode.
   */
  Survival = "survival",

  /**
   * `Creative` is creative mode.
   */
  Creative = "creative",

  /**
   * `Adventure` is adventure mode.
   */
  Adventure = "adventure",
}
